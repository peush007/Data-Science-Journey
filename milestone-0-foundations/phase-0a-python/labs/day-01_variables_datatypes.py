# String variables
my_name = "Peuh Khajuria"
my_city = "Gujrat"
my_university = "Parul university"

# Integer variables
my_age = 20
current_year = 2026
total_students = 60

# Float variables
my_gpa = 8.5
pi_value = 3.14159
model_accuracy = 0.94

# Boolean variables
is_student = True
has_laptop = True
is_employed = False

# Print all variables
print("Name:", my_name)
print("City:", my_city)
print("Age:", my_age)
print("GPA:", my_gpa)
print("Student:", is_student)

# Check data types
print(type(my_name))
print(type(my_age))
print(type(my_gpa))
print(type(is_student))

# Multiple assignment
